type IconType = "fab" | "fad" | "fal" | "far" | "fas"

interface iconProps {
  className?: string
  icon: BrandIcons | RegularIcons
  size?: "xs" | "sm" | "lg" | "2x" | "3x" | "5x" | "7x" | "10x"
  type?: IconType
  duoTone?: boolean
  spin?: boolean
  sharp?: boolean
  thin?: boolean
}

function Icon(props: iconProps) {
  const type = getType(props.type, props.icon)
  const icon = `fa-${props.icon}`
  const size = props.size ? `fa-${props.size}` : ""
  const spin = props.spin ? `animate-spin` : ""
  const sharp = props.sharp ? "fa-sharp" : ""
  const thin = props.thin ? "fa-thin" : ""
  const duoTone = props.duoTone ? "fa-duotone" : ""
  const className = `${
    props.className ? props.className : ""
  } ${type} ${icon} ${sharp} ${thin} ${duoTone} ${size} ${spin}`.trim()

  return <i className={className} />
}

function getType(type: string | undefined, icon: BrandIcons | RegularIcons) {
  if (type === undefined) {
    return "far"
  }

  return isBrand(icon) ? "fab" : type
}

